		<div class='about-part' id='about-part1'>
			<p class="paragraph">One of the most highly appreciated young actresses in Romania, Ana Popescu was born in 1983, in Bucharest. 
			She was raised among paintings, books and movies, which sprinkled magic throughout her childhood years. 
			She was only 5 when she watched her first movie, “Once upon a time in America”.</p>
		
			<p class="paragraph">She defines herself as ethnically ambiguous as her roots originate from all over the world. 
			An almost ironic blend of Hungarian, Greek, Israeli, Persian and Mongolian genes,  Ana dignifies her multiple legacies by being an avid lover of cultures, art and languages. 
			She is fluent in English, Romanian, and French and has working knowledge of Spanish and Greek.</p>
			<div class='clear'></div>
		</div>
		
		<div class='about-part' id='about-part2'>
			<img src='/img/about/004.jpg' />
		
			<p class="paragraph">After fancying studies in archeology, medicine and at the police academy, she settled for a career in chemistry. 
			Her life changed drastically one night when she watched a play by Anton Chekhov  “Uncle Vanya” at Bulandra Theatre in Bucharest. 
			From that moment on, she decided she wanted to become an actress. 
			<br/><br/>
			She studied Acting at the Spiru Haret University of Bucharest under the scrupulous guidance of Professor Sanda Manu, 
			who has trained and educated generations of critically acclaimed stage and screen actors. </p>
			<div class='clear'></div>
		</div>
	
		<div class='about-part' id='about-part3'>
			<p class="paragraph">Ana’s  theater education base includes the Stanislavsky method and the Meisner method. 
			In 2010 she studied at The Lee Strasberg Institute in NYC, where she fell in love with New York City (and currently resides). 
			<br/><br/>
		   	Since, Ana has featured in various commercials, theater plays, and feature films all over the world with renown actors and directors, 
		   	many of whom have encouraged Ana to pursue her acting endeavors abroad. At present, 
		   	she studies with Anthony Abeson and dances every day from jazz to hip-hop to contemporary dance at the Broadway Dance Center in NYC.</p>
	
